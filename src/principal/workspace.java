    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
       if(jTextField1.getText().equalsIgnoreCase("")){
            JOptionPane.showMessageDialog(this, "Ingrese Una Ruta Valida");
            
        }else
           if(!new File(jTextField1.getText()).isDirectory()){
                JOptionPane.showMessageDialog(this, "No es un Directoria Valido");
           }
       else 
       {
                    try
                   {


                       String rutaEspacio=jTextField1.getText();
                       File archivo=new File("Espacio.txt");
                       if(archivo.exists()){
                               archivo.delete();
                           System.out.println("se elimino");

                       }
                       FileWriter escribir=new FileWriter(archivo,true);
                       escribir.write(rutaEspacio);
                       escribir.close();
                       
                       
                       IDE i =new IDE();
                       i.setLocationRelativeTo(null);
                       String m=System.getProperty("user.name");
                       i.setTitle("Jasmo-Studio    Usuario: "+m+ "  Espacio de Trabajo: "+rutaEspacio+"  PIC16F887");
                       i.setSize(915, 680);
                       i.setVisible(true);


               }


               catch(Exception e)
               {
               System.out.println("Error al escribir");
               }
            this.dispose();
       }
    }//GEN-LAST:event_jButton2ActionPerformed

